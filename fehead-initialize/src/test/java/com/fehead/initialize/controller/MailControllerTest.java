@RunWith(SpringRunner.class)
@SpringBootTest(classes = InitializeApplication.class)
public class MailControllerTest {

    @Autowired
    private WebApplicationContext wac;

    private MockMvc mockMvc;

    @Before
    public void setup() {
        mockMvc = MockMvcBuilders.webAppContextSetup(wac).build();
    }

    /**
     * 测试发送邮件
     * @throws Exception
     */
    @Test
    public void sendEmail() throws Exception {
        String result = mockMvc.perform(MockMvcRequestBuilders.post("/api/v1.0/sys/email/send")
                .param("address", "1948730080@qq.com")
                .param("action", "register")
                .contentType(MediaType.APPLICATION_JSON_UTF8))
                .andExpect(MockMvcResultMatchers.status().isOk())
                .andReturn().getResponse().getContentAsString();
        System.out.println(result);
    }

    /**
     * 测试验证邮件
     * @throws Exception
     */
    @Test
    public void validateEmail() throws Exception {
        String result = mockMvc.perform(MockMvcRequestBuilders.put("/api/v1.0/sys/email/validate")
                .param("address", "1948730080@qq.com")
                .param("code", "2801")
                .contentType(MediaType.APPLICATION_JSON_UTF8))
                .andExpect(MockMvcResultMatchers.status().isOk())
                .andReturn().getResponse().getContentAsString();
        System.out.println(result);
    }
}
